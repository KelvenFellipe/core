export function colors(material: string){
 if (material === "Noble gases") return "bg-blue-400"
 if (material === "Transition metals") return "bg-purple-600"
 if (material === "Alkali metals") return "bg-teal-500"
 if (material === "Alkaline earth metals") return "bg-red-600"
 if (material === "Reactive non-metals") return "bg-blue-600"
 if (material === "Metalloids") return "bg-amber-500"
 if (material === "Post-transition metals") return "bg-lime-600"
 if (material === "Actinides") return "bg-orange-500"
 if (material === "Lanthanides") return "bg-violet-700"
 if (material === "Unknown properties") return "bg-zinc-600"
}