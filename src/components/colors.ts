export function colors(material: string){
 if (material === "Noble gases") return "bg-blue-400"
 if (material === "Transition metals") return "bg-indigo-600"
 if (material === "Alkali metals") return "bg-teal-800"
 if (material === "Alkaline earth metals") return "bg-red-600"
 if (material === "Reactive non-metals") return "bg-blue-600"
 if (material === "Metalloids") return "bg-yellow-600"
 if (material === "Post-transition metals") return "bg-green-600"
 if (material === "Actinides") return "bg-orange-600"
 if (material === "Lanthanides") return "bg-violet-600"
 if (material === "Unknown properties") return "bg-zinc-600"
}